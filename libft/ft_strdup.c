#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*copy;
	size_t	i;
	size_t	len;

	len = ft_strlen(str);
	i = 0;
	copy = malloc((len + 1) * sizeof(char));
	if (!copy)
		return (0);
	while (str[i] != '\0')
	{
		copy[i] = str[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
